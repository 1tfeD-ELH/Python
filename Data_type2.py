#List 리스트
my_list = []
my_list.append(123) #[123]
my_list.append('abc') #[123, 'abc']
my_list.append(True) #[123, 'abc', True]

#Tuple 튜플
my_tuple1 = ()
my_tuple2 = (1,)
my_tuple3 = ('a', 'b', 'c')
my_tuple4 = 3.14, 'Python', True

#Dictionary 딕셔너리
my_dict = {} #{}
my_dict[1] = 'a' #{1:'a'}
my_dict['b'] = 2 #{1:'a', 'b':2}
my_dict['c'] = 'd' #{1:'a', 'b':2, 'c':'d'}